int	ft_iterative_factorial(int nb)
{
	int c;
	int d;

	c = 1;
	d = 1;
	if (nb < 0 || nb >= 13)
		return (0);
	if (nb == 0 || nb == 1)
		return (1);
	while (c < nb)
	{
		c = c + 1;
		d = d * c;
	}
	return (d);
}
